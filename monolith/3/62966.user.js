// listen for ALT-R
document.addEventListener('keydown', function(event) {
	if(event.altKey && event.keyCode == 82) {
			window.getSelection().getRangeAt(0).commonAncestorContainer.innerHTML = romajify(window.getSelection().getRangeAt(0).commonAncestorContainer.innerHTML)
	}
}, true);


// take some kana/kanji html string and return the romaji version
function romajify(str) {
	//store the orignal and altered html strings
	var altered_s = str;	
	var orig_s = str;	
	if(orig_s && altered_s){
		
		//replace japanese punctuation with roman punctuation
		for(x in punctuation_list)
		  altered_s = altered_s.replace(new RegExp(punctuation_list[x][1], "gm"), punctuation_list[x][0]);
		
		//replace jouyou kanji with hiragana/katakana
		for(x in joyou_kanji_list)
		  altered_s = altered_s.replace(new RegExp(joyou_kanji_list[x][1], "gm"), joyou_kanji_list[x][0]);
		
		//romanize gojoun tuples
		for(x in gojoun_list)
			altered_s = altered_s.replace(new RegExp(gojoun_list[x][1], "gm"), gojoun_list[x][0]);
		
		//romanize regular hiragana/katakana
		for(x in reg_kana_list)
			altered_s = altered_s.replace(new RegExp(reg_kana_list[x][1], "gm"), reg_kana_list[x][0]);
		
		//geminate double consonants (sokuon)
		for(x in sokuon)
			altered_s = altered_s.replace(new RegExp(sokuon[x][1]+"([bcdfghjklmnpqrstvwxyz])", "gm"), "$1$1");
		
		//romanize small hiragana/katakana
		for(x in small_kana_list)
			altered_s = altered_s.replace(new RegExp(small_kana_list[x][1], "gm"), small_kana_list[x][0]);
	
	
			
		//return the romanized version	
		return altered_s
	}		
}
