function enterNewProxy(){
	document.getElementById('new_proxies').focus();
	document.getElementById('new_proxies').select();
	GM_setValue("proxiesbackup",proxies.toString());
	proxies=(document.getElementById('new_proxies').value+",").replace(/http\:\/\//g,"");
	proxies=proxies.replace(/\/,/g,",").replace(/,$/,"").split(",");
	proxies.sort();
	//remove duplicate
	var j;
	for(j=1;j<proxies.length;j++){
		if(proxies[j-1]==proxies[j]){proxies.splice(j,1);j--;}
	}
	if(debug) alert(proxies);
	GM_setValue("workingProxies",proxies.toString());
}

