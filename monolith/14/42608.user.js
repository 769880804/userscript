function setLanguage() {
	var arr = server.split("\.");
	var lang = arr[arr.length - 1];
	if (lang == "com" && arr.length == 4) { //for example: http://s1.ba.ikariam.com
		lang = "en";
	}
	var l = getCfgValueNonEmpty("LANGUAGE", language);
	if (l != undefined) {
		lang = l;
	}
	return lang;
}

