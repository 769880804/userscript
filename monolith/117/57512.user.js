function loadList(){
	var _cd = new Date();
	var _ye = _cd.getFullYear();
	var _mo = _cd.getMonth();
	var _da = _cd.getDate()
	var _ho = _cd.getHours();
	var _tz = (_cd.getTimezoneOffset() * 60) + -30;
	var _mi = _cd.getMinutes();
	var _se = _cd.getSeconds();
	var _nd = new Date(Date.UTC(_ye,_mo,_da,_ho,_mi,_se));
	var _ts =  (_nd.getTime() / 1000) + _tz;
	var _lu = _nlu.replace("{0}", _ts);
	$.get(_lu,'',function(returned_data){
		var _rd = returned_data.split('for (;;);');
		eval('var bbb = ' + _rd[1]);
		$('#ajaw-list').html(bbb.payload.html);
		$('#ajaw-list').find('img, .UIIntentionalStory_Pic, .commentable_item, .UIIntentionalStory_Header, .UIStoryAttachment_BlockQuote').remove();
		$('#ajaw-list > .UIStream > .UIStory').each(function(i){
			var _an = $(this).find('.CopyTitle').html();
			if (_an){
				_an.toString();
			}else{
				_an = ""
			}
			$(this).find('a').each(function(i){
				var url_array = getArgs($(this).attr('href'));
				if (url_array.sendkey != undefined){
					if (_caa[url_array.ztrack_category]){
						if (!_uaa[url_array.sendkey] && !_ua[url_array.sendkey]){
							_ua[url_array.sendkey] = url_array;
							_ua[url_array.sendkey]['url'] = $(this).attr('href');
							_ua[url_array.sendkey]['anotation'] = _an;
						}
					}
				}
			});
		});
	});
	listEvents();
}

