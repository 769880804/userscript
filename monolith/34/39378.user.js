function makeTooltip(term) {
  var comments = document.evaluate(term, document, null, XPathResult.ORDERED_NODE_SNAPSHOT_TYPE, null);
  for (var i = 0; i < comments.snapshotLength; i++)
    comments.snapshotItem(i).innerHTML = comments.snapshotItem(i).innerHTML.replace(threelettercode, fullname);
}

var twolettercode = /\b[!=]*[!"]*[A-Z]{2}\b/g;
var fullname = function (code) {
  if (airline[code] == undefined) return code;
  else return "<span class=\"ft_code\">"+code+"<span class=\"ft_name\">"+airline[code]+"</span></span>";
}

