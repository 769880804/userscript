function s_calc()
{
  var loop_input = main_frame.contentWindow.document.getElementById('s_loop_count');
  var loop_count = loop_input.value;  
  var product_table = inner_tables[2];
  var product_tr = product_table.getElementsByTagName('tr');
  var fab_index = fab_product[r_index].indexOf(product_tr[0].childNodes[1].innerHTML);
  if(fab_index != -1)
  {
    var product_count = (product_tr[0].childNodes[5].childNodes[0].innerHTML);
    var prod_loops = Math.floor(fab_product_coef[fab_index][1] / fab_product_coef[fab_index][0]);
    if(product_count != 0)
    {
      var allready_looped = Math.ceil(product_count / fab_product_coef[fab_index][0]);
      var product_more = product_count - ( Math.floor(product_count / fab_product_coef[fab_index][0]) * fab_product_coef[fab_index][0]);
      product_tr[0].childNodes[5].innerHTML += '<br>(' + product_more + ')';
      product_title_tr[1].innerHTML += '<td class="hdr">Прошло циклов: ' + Math.floor(product_count / fab_product_coef[fab_index][0]) + '</td>';
      product_title_tr[1].innerHTML += '<td class="hdr">Циклов до конца: ' + (prod_loops - Math.ceil(product_count / fab_product_coef[fab_index][0])) + '</td>';
      if(main_frame.contentWindow.document.getElementById('s_sloop_count').value != 0)
        main_frame.contentWindow.document.getElementById('s_sloop_count').value = (prod_loops - Math.ceil(product_count / fab_product_coef[fab_index][0]));
      
    }
    for(i = 0; i < fab_resource[fab_index].length; i++)
    {  
      var res_table = inner_tables[4 + i];
      var res_tr = res_table.getElementsByTagName('tr');
      var res_index = fab_resource[fab_index].indexOf(res_tr[0].childNodes[1].innerHTML)
      if(res_index != -1) 
      {
        var res_count = res_tr[0].childNodes[5].childNodes[0].innerHTML;
        var res_per_loop = (prod_loops * fab_resource_coef[fab_index][res_index][0]) * loop_count;
        var need_res = res_per_loop - res_count;
        if((prod_loops - Math.ceil(product_count / fab_product_coef[fab_index][0])) != 0)
        {
           need_res = res_count - (prod_loops - Math.ceil(product_count / fab_product_coef[fab_index][0]))*fab_resource_coef[fab_index][res_index][0];
        } 
        res_tr[0].childNodes[5].childNodes[0].innerHTML += '(' + need_res + ')'; 
      } 
    }
    if(fab_aresource[fab_index] != null)
    {
      for(i = 0; i < fab_aresource[fab_index].length; i++)
      {
        var ares_table = inner_tables[5 + fab_resource[fab_index].length + i];
        var ares_tr = ares_table.getElementsByTagName('tr');       
        var ares_index = fab_aresource[fab_index].indexOf(ares_tr[0].childNodes[1].innerHTML)
        if(ares_index != -1) 
        {
          var ares_count = ares_tr[0].childNodes[5].childNodes[0].innerHTML;
          var ares_per_loop = (prod_loops * fab_aresource_coef[fab_index][ares_index][0]) * loop_count;
          var need_ares = ares_per_loop - ares_count;
          ares_tr[0].childNodes[5].childNodes[0].innerHTML += '(' + need_ares + ')'; 
        } 
      }
    } 
  }
}



