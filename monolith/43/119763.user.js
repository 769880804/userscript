var replaceText = function(reference) {
  var path = 'div/a[@id="variable_lp"]/div[@id="cn-bold-blue-text"]';
  var divs = document.evaluate(path, reference, null,
    XPathResult.UNORDERED_NODE_SNAPSHOT_TYPE, null);

  for (var i = 0; i < divs.snapshotLength; i++) {
    divs.snapshotItem(i).innerHTML =
      'Please read:<br />' +
      'A personal appeal from<br />' +
      'VersaLife founder Bob Page';
  }
};

