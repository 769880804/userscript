// ==UserScript==
// @name           Improved Java 7 Javadoc style
// @namespace      http://java.com/java7doc-fix
// @grant          none
// @include        http://docs.oracle.com/javase/7/docs/api/*
// @include        */doc/*
// @include        */docs/*
// @include        */apidoc/*
// @include        */apidocs/*
// @include        */javadoc/*
// @include        */javadocs/*
// @include        */api/*
// @include        */api-*
// @version        1.1
// ==/UserScript==

var head = document.getElementsByTagName("HEAD")[0];

var javadoc7 = head.innerHTML.contains("Generated by javadoc (version 1.7");
var javadoc8 = head.innerHTML.contains("Generated by javadoc (version 1.8");
var oracledoc = location.toString().contains("http://docs.oracle.com/javase/7/docs/api/");

if (javadoc7 || javadoc8 || oracledoc) {
    var styleElement = head.appendChild(window.document.createElement('style'));
    styleElement.innerHTML = '@import "http://people.apache.org/~ebourg/java7doc-fix.css";';
}
