function matchCallback($0,$1){
	var sl=$0.length;
	if(sl<=3)return $0;
	var wt=words[$0];
	if(!wt){
		var last=$0.charAt(sl-1);
		switch(last){
			case 's': 
			wt=words[$0.substr(0,sl-1)];
			if(!wt && $0.charAt(sl-2)=='e'){
				wt=words[$0.substr(0,sl-2)];
			}
			break;
			case 'd':
			if($0.substring(sl-2)=='ed'){
				wt=words[$0.substr(0,sl-2)];
				if(!wt){
					wt=words[$0.substr(0,sl-1)];
				}
			}
			break;
			case 'y':
			if($0.substring(sl-2)=='ly'){
				wt=words[$0.substr(0,sl-2)];
			}
			break;
			case 'g':
			if($0.substring(sl-3)=='ing'){
				wt=words[$0.substr(0,sl-3)];
				if(!wt && $0.charAt(sl-4)==$0.charAt(sl-5)){
					wt=words[$0.substr(0,sl-4)];
				}
			}
			break;
		}
	}
	if(wt>0){
		if(wt==1)
			return '<b><font color="blue">'+$0+'</font></b>';
		else
			return '<font color="blue">'+$0+'</font>';
	}
	return $0;
}

function traverse(element) {
	var nodeName=element.nodeName;
	if(nodeName=='TEXTAREA'){
		return;
	}
	for (var child=element.firstChild; child!==null; child=child.nextSibling) {
		if (child.nodeType===1){
			traverse(child);
		} else if (child.nodeType===3){
			var value=child.nodeValue;
			var newValue=value.replace(/\b([a-zA-Z]+)\b/g,matchCallback);
			if(newValue!=value){
				if(element.childNodes.length==1){
					element.innerHTML=newValue;
				}else{
					var newNode=document.createElement('span');
					newNode.innerHTML=newValue;
					element.replaceChild(newNode,child);
				}
			}
		}
	}
}

