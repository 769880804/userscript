console.log('executing hikr_add_gallery greasemonkey script');


unsafeWindow.newPageHtml = createNewPageHtml();
function openFullScreenGallery() {
    //console.log(unsafeWindow.newPageHtml);
    window.open("javascript:opener.newPageHtml");
}


fsGalleryLink = $("<a/>", {'id': 'FSGalleryLink'}).append('FSGallery')
fsGalleryLink.insertAfter($("img[src='http://s.hikr.org/r4icons/slideshow.png']"));

var userMenuLi = $('li#li_home_menu');
var loggedIn = userMenuLi.length != 0;
if(loggedIn) {
    var userPage = userMenuLi.children('a').attr('href');
    var userInfoPageUrl = userPage + 'userinfo.html';
    var hikrAccount = userPage.replace('http://www.hikr.org/user/','');
    var hikrAccount = hikrAccount.substr(0,hikrAccount.length-1);
    $.get(userInfoPageUrl, function(data) {
        var infoImg = $('img[src="http://s.hikr.org/r4icons/information.png"]', $(data));
        var accountType = infoImg.parent().parent().next().children('span').text();
        if (accountType =='Pro') {
            fsGalleryLink.click(openFullScreenGallery)
        } else {
            fsGalleryLink.click(function(){alert('not a pro user');});
        }
    });
} else {
    fsGalleryLink.click(function(){alert('not logged in');});
}



