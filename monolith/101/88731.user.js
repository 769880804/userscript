function findObject(tag, attribute, search, redirect)
{

	var allFoundItems = document.body.getElementsByTagName(tag);
	for (i = 0; i < allFoundItems.length; i++)
	{
		try
		{
			if (allFoundItems[i].getAttribute(attribute).match(search))
			{	
				if (redirect == undefined )
				{
					removeAttributes(allFoundItems[i]);
					addImageElement(allFoundItems[i]);
				}
				else if (redirect == "hrefRedirect")
					AddIMG(allFoundItems[i].href);
			}
		} catch (err) {}
	}
}


