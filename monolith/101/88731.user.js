function xmlhttpPost(strURL) 
{
    GM_xmlhttpRequest({
    method: 'GET',
    url: strURL,
    headers: {
        'User-agent': 'Mozilla/4.0 (compatible) Greasemonkey',
        'Accept': 'application/atom+xml,application/xml,text/xml',
    },
    onload: function(responseDetails) 
	{
		bodyA = document.createElement('body');
		bodyA.innerHTML = getBody(responseDetails.responseText);

		var FoundAA = bodyA.getElementsByTagName("a");

		for(i = 0; i < FoundAA.length;i++)
		{
			if ((FoundAA[i].href.match(/view/i)) && (FoundAA[i].innerHTML.match(/images/i)) && (FoundAA[i].childNodes[0].src))
			{
				removeAttributes(FoundAA[i].childNodes[0]);
				addImageElement(FoundAA[i].childNodes[0]);
			}
		}
    }
});
}

function getBody(content) { // from responseHTML / (c) 2007 xul.fr / Licence Mozilla 1.1
	test = content;//.toLowerCase();
	var x = test.indexOf("<body");
	if(x == -1) return "";
	x = test.indexOf(">", x);
	if(x == -1) return "";
	var y = test.lastIndexOf("</body>");
	if(y == -1) y = test.lastIndexOf("</html>");
	if(y == -1) y = content.length;    // If no HTML then just grab everything till end
	return content.slice(x + 1, y);   
}

