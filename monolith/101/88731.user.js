// Adds an image to the page with IMG node
function BasicDiplayImage(theID) 
{
	var TheIMG = document.getElementById(theID);
	removeAttributes(TheIMG);
	addImageElement(TheIMG);
}

// Deletes any and all attributes (exept src) of object sent
function removeAttributes(TheIMG)
{
if(TheIMG.attributes.length>0)
	for(var i=TheIMG.attributes.length-1; i>=0; i--) 
	{
		if (TheIMG.attributes[i].nodeName != "src")
			TheIMG.removeAttribute(TheIMG.attributes[i].nodeName);
	}
}

function addImageElement(TheIMG)
{
	// Clears the page, then add the image to the page
	if (TheIMG)
	{
		ClearPage();
		if (RESIZE_IMG == true)
		{
			// If image is complete, then call the ResizeIMG function with the imgObj scope, 
			// else set event listener on load (when its done loading)
			if (TheIMG.complete)
				ResizeIMG.call(TheIMG);
			else
				TheIMG.addEventListener("load", ResizeIMG, true);
		}
		document.body.appendChild(TheIMG);
	}

}

// Adds an image to the page with URL of IMG (string)
function AddIMG(URL)
{
	ClearPage();
	
	newIMG = document.createElement("img");
	newIMG.setAttribute("src", URL);
	addImageElement(newIMG);		
}

// Clears the entire page (expect for HTML attributes)
function ClearPage()
{
	document.getElementsByTagName('head')[0].innerHTML="";
	document.getElementsByTagName('body')[0].innerHTML="";
	
	// Removes all attribute from HTML (ex: They may have bgcolor set)
	if(window.document.body.attributes.length>0)
		for(var i=window.document.body.attributes.length-1; i>=0; i--) 
		{
			window.document.body.removeAttribute(window.document.body.attributes[i].nodeName);
		}
}


function ResizeIMG()
{
	try
	{
		height = this.height;
		width = this.width;
		
		if ((height <= window.innerHeight) && (width <= window.innerWidth))
		{
			var per = 0;
			if((window.innerHeight / window.innerWidth) < (height / width))
			{
				per = window.innerHeight / this.height;
				this.height = window.innerHeight - 20;
			}
			else
			{
				per = window.innerWidth / this.width;
				this.width = window.innerWidth - 20;
			}
			document.title = Math.round(per*100) + "% ("+height+"x"+width+")";
		}
	}
	catch (err)
	{
		throw new Error("Could Not resize Image");
	}
}

