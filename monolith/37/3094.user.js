var DOWN = 40;
var UP   = 38;
var key_codes = [DOWN,DOWN,UP,UP,DOWN,DOWN,DOWN,UP,UP,UP,UP]
var key_index = 0;
var interval = null;

get_bottom = function() {
	var elem = document.documentElement
	var y = elem.scrollTop + window.innerHeight - 250;
	return(y);
}
get_width = function() {
	var width = toasty_div.style.width;
	width = width.replace(/px/,'');
	width = new Number(width);
	return(width);
}
animate_in = function() {
	var width = get_width();
	width = width + toasty_step;
	var y = get_bottom();
	if(width <= toasty_img.width) {
	  toasty_div.style.top  = y + 'px';
	  toasty_div.style.width = width + 'px';
	} else {
	  if(interval) {
		clearInterval(interval);
		interval = setInterval(animate_wait,toasty_pause);
	  }
        }
}
animate_wait = function() {
  clearInterval(interval);
  interval = setInterval(animate_out,toasty_interval);
}
animate_out = function() {
	var width = get_width();
	width = width - toasty_step;
	var y = get_bottom();
	if(width >= 0) {
	  toasty_div.style.top  = y + 'px';

	  toasty_div.style.width = width + 'px';
        } else {
	  if(interval)
		clearInterval(interval);
		document.documentElement.removeChild(toasty_div);
        }
}

function key_watcher(event){
  if(event.keyCode == key_codes[key_index]) {
	key_index++;
	if(key_index == key_codes.length) {
		document.documentElement.appendChild(toasty_div);
		document.documentElement.appendChild(toasty_sound);
		interval = setInterval(animate_in,toasty_interval);
		key_index = 0;
        }
	
  }
}

window.addEventListener(
    "keyup",
    key_watcher,
    true);