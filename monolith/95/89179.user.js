function lightenColor(col, amt){
	var r = col.substring(1, 3);
	var g = col.substring(3, 5);
	var b = col.substring(5, 7);
	r = parseInt(r, 16);
	g = parseInt(g, 16);
	b = parseInt(b, 16);
	r = Math.max(0,Math.min(r+amt, 255));
	g = Math.max(0,Math.min(g+amt, 255));
	b = Math.max(0,Math.min(b+amt, 255));
	r = r.toString(16);
	if(r.length==1)
		r = "0"+r;
	g = g.toString(16);
	if(g.length==1)
		g = "0"+g;
	b = b.toString(16);
	if(b.length==1)
		b = "0"+b;
	var newcol = '#'+r+g+b;
	return newcol;
}

