function notify2(){ 
//get numbers from 'X minutes, Y seconds left' text 
var reParse = /[0-9]+/g; var match = document.getElementById('button').textContent.match(reParse);

//convert numbers pulled from text into minutes/seconds
var minutes = 0;
var seconds = 0;
if(match.length >= 1){
    minutes = parseInt(match[0]);
}
if(match.length >= 2){
    seconds = parseInt(match[1]);
}

//number of seconds remaining
seconds += minutes * 60;

//set timeout for 1 minute to click
if(seconds > 60){
     setTimeout("alert('Time to click soon!')",(seconds-60)*1000);
}
} 
notify2();