<div id='full_description'><p>Change Log:
<br />Version 5.13.2012.3
<br />Changed the Farm method completely.
<br />Separated the Farm and Artifact Functions.
<br />Modified the Option tab.
<br />Modified the Search method.
<br />Modified the Build method.
<br />Modified the Tower Method. (Hopefully works now)
<br />Messed up the overview tab (Will make it look pretty again soon)</p>

<p>Version 5.06.2012.2
<br />Added Crate redeeming in Options Tab.
<br />Training tab now queues only 2 units each pass.
<br />Scouting should now automatically switch between cities.
<br />Scout Tab still in beta.</p>

<p>Version 4.15.2012.1
<br />Added Scout Tab
<br />Scout Tab is in beta.</p>

<p>Versions 4.11.2012.1
<br />Transports can now be given a name.
<br />Reverted back to the Text style, and removed the icons from the train tab.
<br />Resorted the Units on both the train and overview tab to be how they are sorted in game.
<br />Train tab only fills 1 queue at a time, it fills it at the finish of the last training. Still working on this as always.</p>

<p>Version 3.31.2012.1</p>

<p>Version 3.23.2012.1
<br />Train tab Enable/Disable Button now changes color.
<br />Farm tab "Start from beginning of target list" button should now work as intended.
<br />Anytime the Target list resets to the beginning there should now be a log entry for it.</p>

<p>Version 3.21.2012.4
<br />Copying a list of coordinates to the farm tab should reset the pointer back to 0 (Essentially starting the attacks from the top of the list again)
<br />Training Queues should only fill up to 10.
<br />Stopping the script at the end of the Coordinate list should reset it back to the beginning.</p>

<p>Older Versions.
<br />Changed the main color to purple (not sure how I feel about this one.)
<br />Changed separators to commas
<br />When you are low on food it now changed to a bold red color and blinks</p></div>

