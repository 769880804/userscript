function keydh(e) {
	if (workingJKR == true) return;
	if (!e) {
		e = event;
	}
	if (e.altKey && (e.keyCode == 75)) {
		var element = document.body;
		if (e.element) {
			element = e.element();
			if (element.tagName == 'INPUT' || element.tagName == 'TEXTAREA') {
				if (e.keyCode == 0) { 
					workingJKR = false;
					return;
				}
			}
		}
		workingJKR = true;

		var css = document.createElement("style");
		css.type = "text/css";
		css.innerHTML = "#KanaRomaji {color: black; z-index: 10; } #KanaRomaji:hover { color: white; background: blue; } .KanjiN5 { background: #7FFFD4; } .Kana { background: #FDFCDC; } .KanjiNN { background: #FFDDDD; }";
		document.body.appendChild(css, null);

		// Replace in <A>
		var allLinks = document.getElementsByTagName("a");
		for (i=0; i<allLinks.length; i++) 
		{
			if (allLinks[i].title !== undefined) allLinks[i].title = '';
			if (allLinks[i].alt !== undefined) allLinks[i].alt = '';
		}
		// Replace in <IMG>
		var allImages = document.getElementsByTagName("img");
		for (i=0; i<allImages.length; i++) 
		{
			if (allImages[i].title !== undefined) allImages[i].title = '';
			if (allImages[i].alt !== undefined) allImages[i].alt = '';
		}
		// Replace in <INPUT>
		var allInput = document.getElementsByTagName("input");
		for (i=0; i<allInput.length; i++) 
		{
			if (allInput[i].title !== undefined) allInput[i].title = '';
			if (allInput[i].alt !== undefined) allInput[i].alt = '';
			if (allInput[i].value !== undefined) allInput[i].value = convertJP(allInput[i].value, "kana", false);;
		}
		// Replace in <BUTTON>
		var allButtons = document.getElementsByTagName("button");
		for (i=0; i<allButtons.length; i++) 
		{
			if (allButtons[i].title !== undefined) allButtons[i].title = '';
			if (allButtons[i].alt !== undefined) allButtons[i].alt = '';
		}

		innetHTML_work = element.innerHTML; // Get HTML

		innetHTML_work = convertJP(innetHTML_work, "all", true); // Convert the rest

		element.innerHTML = innetHTML_work; // Return HTML
	
		var idKR = document.getElementsByTagName("span");
		for (i = 0; i < idKR.length; i++)
		{
			if(idKR[i].id == 'KanaRomaji') set_toolbox(idKR[i]); // Make a Tooltip
		}

	}
}

function wait(millisecond) {
	var date = new Date();
	var current_date = null;

	do { current_date = new Date(); }
	while(current_date-date < millisecond)
}

function init() {
	if (document.addEventListener) {
	   document.addEventListener("keydown", keydh, true);
	}
	else if (document.attachEvent) {
	   document.attachEvent("onkeydown", keydh);
	}
	else {
	   document.onkeydown = keydh;
	}
}

function locate_cursor(event)
{
	var posx, posy, realx, realy;
	var d = find_windowbox();
	maxWidth = document.body.clientWidth;
	maxHeight = document.body.clientHeight;
	posx = event.clientX + window.pageXOffset;
	posy = event.clientY + window.pageYOffset;
	realx = posx + d.offsetWidth + 15;
	if(realx >= maxWidth) {
		realx = maxWidth - d.offsetWidth;
		if(maxWidth - posx <= d.offsetWidth) {
			posx = posx - d.offsetWidth - 10; // left
		} else {
			posx = realx; // right
		}
	} else if (realx <= 0) {
		posx = 0;
	} else {
		posx = realx - d.offsetWidth;
	}
	realy = posy + d.offsetHeight - 45;
	if(realy >= maxHeight) {
		posy = maxHeight - d.offsetHeight;
	} else if ((realy - d.offsetHeight) <= 0) {
		posy = d.offsetHeight;
	} else {
		posy = realy - d.offsetHeight;
	}
	d.style.top = posy + "px";
	d.style.left = posx + "px";
}
	
function find_windowbox()
{
	return document.getElementById("KanaRomaji_tt");
}
	
function create_windowbox(id)
{
	var tt_div = document.createElement("div");
	tt_div.setAttribute("id", "KanaRomaji_tt");
	tt_div.setAttribute("style", "background:#EAEAEA;border:1px solid #D5D5D5;padding:4px;color:#000000;font-family:tahoma;font-size:20px;position:absolute;z-index:1000;max-width:590px;");
	var tt_title = document.createTextNode(removeHTML(id.innerHTML) + "\n -> " + id.title);
	tt_div.appendChild(tt_title);
	document.body.appendChild(tt_div);
}

function removeHTML(val) 
{
	return val.replace(/<(?:.)*?>/gm, '');
}
	
function kill_windowbox()
{
	if (find_windowbox()) find_windowbox().parentNode.removeChild(find_windowbox());
}

function set_toolbox(id)
{	
	id.addEventListener("mouseover", function() { create_windowbox(id); }, false);
	id.addEventListener("mouseout", function() { kill_windowbox(); }, false);
	id.addEventListener("mousemove", function(event) { locate_cursor(event); }, true);
}

