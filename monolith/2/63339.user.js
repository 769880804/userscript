(function() {
	formOb = document.getElementsByTagName("form")[0];
	if(formOb.addEventListener)	{ // Standard
		formOb.addEventListener('submit', checkwhere, false);
		return true;
	} else if(formOb.attachEvent) { // IE
		formOb.attachEvent('on' + submit, checkwhere);
		return true;
	} else return false;
	
	function checkwhere() {
		str = document.getElementsByName('q')[0].value;
		command = str.substring(0,2);
		query = str.substring(3);
		if (command == ";i") {
			window.location = "http://images.google.com/images?&q="+query;
		}
		else if (command == ";w") {
			window.location = "http://wikipedia.org/wiki/"+query;
		}
		else if (command == ";v") {
			window.location = "http://video.google.com/videosearch?q="+query;
		}
		else if(command == ";y") {
			window.location = "http://www.youtube.com/results?search_query="+query;
		}
		else {
		}
		return false;
	}
	
})();