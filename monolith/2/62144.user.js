function smartyPants(){

	function smarty(text){

		text = text.replace(RegExp('([^'+l+']|^)"(['+l+'])', 'g'), '$1&laquo;$2'); //1 for double
		text = text.replace(RegExp('(['+l+'\\.\\?!])"([^'+l+']|$)', 'g'), '$1&raquo;$2'); //2 for double
		text = text.replace(/\.\.\./g, '&#0133;'); //ellipses
		text = text.replace(/-{2,3}/g, '&#8212;'); //em dash

	return text;
	}//nested function to keep alive

	var el = document.getElementById('content_ifr').contentDocument.getElementById('tinymce');
	ta = el.innerHTML;
	ta = ta.replace(/(<([^>]+)>)/ig," ");
	words = ta.split(/\s+/);

	for(i=0;i<words.length;i++){
		el.innerHTML = el.innerHTML.replace(words[i], smarty(words[i]));
	}

}

