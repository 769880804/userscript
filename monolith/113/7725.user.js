  function ACLshowToolTip() {
    if(ACLdefinitions.length) {
      var minX, minY, maxX, maxY, ttX, ttY;

      ACLtooltip.style.width = "auto";
      ACLtooltip.style.height = "auto";
      ACLtooltip.innerHTML = ACLdefinitions;
      ACLtooltip.firstChild.style.marginTop = "0";
      ACLtooltip.firstChild.style.marginRight = "0";
      ACLtooltip.firstChild.style.marginBottom = "0";
      ACLtooltip.firstChild.style.marginLeft = "0";
      ttX = ACLcurX + 10;
      ttY = ACLcurY + 10;
      minX = document.documentElement.scrollLeft;
      minY = document.documentElement.scrollTop;
      maxX = minX + document.documentElement.clientWidth - ACLtooltip.scrollWidth;
      maxY = minY + document.documentElement.clientHeight - ACLtooltip.scrollHeight;
      //GM_log(document.documentElement.clientWidth + "," + ACLtooltip.scrollWidth + "," + minX + "," + minY + "," + maxX + "," + maxY);

      if(ttX < minX)
        ttX = minX;
      else if(ttX > maxX)
        ttX = maxX;
      if(ttY < minY)
        ttY = minY;
      else if(ttY > maxY)
        ttY = maxY;

      ACLtooltip.style.left = ttX + "px";
      ACLtooltip.style.top = ttY + "px";

      ACLtooltip.style.visibility="visible";
    }
  }

  function ACLtranslateWord(input) {
    var ac=ACLairports[input];
    if (ac) {
      ACLdefinitions="<div>"+ac+"</div>";
      ACLshowToolTip();
    }
  }

  function ACLgetWord() {
    //GM_log("i am " + ACLparent);

    //GM_log("i am attributes " + ACLparent.className);
    //GM_log("tag name is " + ACLparent.tagName);
    var range = ACLparent.ownerDocument.createRange();
    range.selectNode(ACLparent);
    var str = range.toString();
    range.detach();

    if(ACLoffset < 0 || ACLoffset >= str.length)
      return null;
    var start = ACLoffset;
    var end = ACLoffset + 1;
    var valid_word = /^([A-Z]+)$/;
    //GM_log(escape(str.substring(start, start + 1)));
    if(!valid_word.test(str.substring(start, end)))
      return null;
    while(start > 0 && valid_word.test(str.substring(start - 1, end)))
      start--;
    while(end < str.length && valid_word.test(str.substring(start, end+1)))
      end++;
    var text = str.substring(start, end);
    //GM_log("try to translate " + text);
    ACLtranslateWord(text);
  }

  function ACLmousescroll(event) {
    ACLmousemove(event);
  }

  function ACLhide() {
    if(ACLtimeoutID) {
      ACLdefinitions = "";
      ACLtooltip.style.visibility = "hidden";
      ACLtooltip.innerHTML = ACLdefinitions;
      ACLtooltip.style.left = 0 + "px";
      ACLtooltip.style.top = 0 + "px";
      ACLtooltip.style.width = 0 + "px";
      ACLtooltip.style.height = 0 + "px";
      window.clearTimeout(ACLtimeoutID);
      ACLtimeoutID = 0;
    }
  }

  function ACLmousemove(event) {
    //test out the tooltip
    var e = event;

    //every movement of the mouse restarts the timer and removes the tooltip
    ACLhide();

    //don't bother starting a new timer if the location is no good.
    try {
      if(!e || !e.rangeParent || e.rangeParent.nodeType != e.rangeParent.TEXT_NODE
         || e.rangeParent.parentNode != e.target)
        return;
    }
    catch(e) {
      return; //I don't know how to handle the permission denied thing
    }

    ACLtimeoutID = window.setTimeout(ACLgetWord, ACLtooltipDelay);
    //variables for use in displaying the translation:
    ACLparent = event.rangeParent;
    ACLoffset = event.rangeOffset;
    ACLcurX=e.pageX;
    ACLcurY=e.pageY;
    return;
  }

  // Initialize the script
